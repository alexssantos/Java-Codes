package infnet.alexssantos.dr4at.model;

public class LoginForm {

    private String nome;
    private String senha;

    public LoginForm() {
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }
}
